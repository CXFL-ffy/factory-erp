-- ============================================================================
-- 创建视图 (方便查询)
-- ============================================================================

-- 客户欠款视图
CREATE OR REPLACE VIEW customer_debt AS
SELECT 
    c.id,
    c.name,
    SUM(o.total_amount - o.paid_amount) as total_debt
FROM customers c
JOIN orders o ON c.id = o.customer_id
WHERE o.total_amount > o.paid_amount
GROUP BY c.id, c.name;

-- 库存预警视图 (库存低于 10 的商品)
CREATE OR REPLACE VIEW low_stock_alert AS
SELECT * FROM products WHERE current_stock < 10;

