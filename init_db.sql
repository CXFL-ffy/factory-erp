-- 1. 用户表 (扩展 Supabase Auth)
CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    role TEXT DEFAULT 'employee', -- admin, employee
    created_at TIMESTAMP DEFAULT NOW()
);

-- 2. 商品表
CREATE TABLE IF NOT EXISTS products (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    base_unit TEXT DEFAULT '斤',
    display_unit TEXT DEFAULT '罐',
    conversion_rate NUMERIC DEFAULT 0.5,
    price_tier_1 NUMERIC DEFAULT 0, -- 大客户价
    price_tier_2 NUMERIC DEFAULT 0, -- 常规价
    price_tier_3 NUMERIC DEFAULT 0, -- 零散价
    current_stock NUMERIC DEFAULT 0,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 3. 库存流水表
CREATE TABLE IF NOT EXISTS inventory_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id BIGINT REFERENCES products(id),
    type TEXT NOT NULL, -- IN, OUT_SALE, OUT_GIFT, OUT_INTERNAL, LOSS, RETURN_GOOD, RETURN_BAD
    quantity NUMERIC NOT NULL,
    date DATE DEFAULT CURRENT_DATE,
    remark TEXT,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 4. 客户表
CREATE TABLE IF NOT EXISTS customers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    phone TEXT,
    address TEXT,
    price_level INT DEFAULT 2, -- 1:大客户，2:常规，3:零散
    latitude NUMERIC,
    longitude NUMERIC,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 5. 订单主表
CREATE TABLE IF NOT EXISTS orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id BIGINT REFERENCES customers(id),
    order_date DATE DEFAULT CURRENT_DATE,
    total_amount NUMERIC DEFAULT 0,
    paid_amount NUMERIC DEFAULT 0,
    payment_method TEXT, -- 微信，支付宝，现金，赊账
    driver_expense NUMERIC DEFAULT 0,
    status TEXT DEFAULT 'pending', -- pending, shipped, delivered
    is_printed BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 6. 订单明细表
CREATE TABLE IF NOT EXISTS order_items (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id BIGINT REFERENCES orders(id),
    product_id BIGINT REFERENCES products(id),
    quantity NUMERIC NOT NULL,
    unit_price NUMERIC NOT NULL,
    is_free_gift BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 7. 原材料表
CREATE TABLE IF NOT EXISTS materials (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    current_price NUMERIC DEFAULT 0,
    stock_quantity NUMERIC DEFAULT 0,
    unit TEXT DEFAULT '斤',
    created_at TIMESTAMP DEFAULT NOW()
);

-- 8. 材料价格表 (爬虫数据)
CREATE TABLE IF NOT EXISTS material_prices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    material_name TEXT NOT NULL,
    price NUMERIC NOT NULL,
    region TEXT,
    price_change TEXT,
    date DATE DEFAULT CURRENT_DATE,
    source TEXT,
    crawl_time TIMESTAMP DEFAULT NOW()
);

-- 9. 政策新闻表 (爬虫数据)
CREATE TABLE IF NOT EXISTS policy_news (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    source TEXT,
    region TEXT,
    summary TEXT,
    url TEXT,
    publish_date DATE,
    crawl_time TIMESTAMP DEFAULT NOW()
);

-- 10. 潮汕企业表
CREATE TABLE IF NOT EXISTS chaoshan_companies (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    industry TEXT,
    phone TEXT,
    address TEXT,
    contact TEXT,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 11. 员工表
CREATE TABLE IF NOT EXISTS employees (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    phone TEXT,
    position TEXT,
    base_salary NUMERIC DEFAULT 0,
    hire_date DATE,
    status TEXT DEFAULT 'active',
    created_at TIMESTAMP DEFAULT NOW()
);

-- 12. 工资表
CREATE TABLE IF NOT EXISTS salaries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id BIGINT REFERENCES employees(id),
    year INT,
    month INT,
    basic_salary NUMERIC DEFAULT 0,
    bonus NUMERIC DEFAULT 0,
    overtime NUMERIC DEFAULT 0,
    social_insurance NUMERIC DEFAULT 0,
    tax NUMERIC DEFAULT 0,
    total_paid NUMERIC DEFAULT 0,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 13. 支出表
CREATE TABLE IF NOT EXISTS expenses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type TEXT NOT NULL, -- driver_expense, material, salary, other
    amount NUMERIC NOT NULL,
    date DATE DEFAULT CURRENT_DATE,
    remark TEXT,
    created_at TIMESTAMP DEFAULT NOW()
);

